#!/usr/bin/env node
/**
 * 从 app.config.mjs 读取品牌配置，并同步到工程文件。
 */

import { readFileSync, writeFileSync } from 'fs'
import { dirname, resolve } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = resolve(__dirname, '..')

const { default: cfg } = await import(
  new URL(`file:///${resolve(root, 'app.config.mjs').replace(/\\/g, '/')}`)
)

const assertNonEmpty = (name, value) => {
  if (typeof value !== 'string' || value.trim().length === 0) {
    throw new Error(`[brand] ${name} 不能为空`)
  }
}

const assertAscii = (name, value) => {
  if (!/^[\x20-\x7E]+$/.test(value)) {
    throw new Error(`[brand] ${name} 必须是 ASCII（系统字段建议英文），当前值：${value}`)
  }
}

// 字段校验
assertNonEmpty('appName', cfg.appName)
assertNonEmpty('appId', cfg.appId)
assertNonEmpty('productName', cfg.productName)
assertNonEmpty('description', cfg.description)
assertNonEmpty('firewallRuleName', cfg.firewallRuleName)

assertAscii('appId', cfg.appId)
assertAscii('productName', cfg.productName)
assertAscii('firewallRuleName', cfg.firewallRuleName)

// 1) src/shared/branding.ts
const brandingTs = [
  '// ⚠️ Auto-generated by scripts/apply-branding.mjs. Do not edit directly.',
  '// To update branding, edit app.config.mjs and run: npm run brand',
  '',
  `export const APP_NAME           = '${cfg.appName}'`,
  `export const APP_ID             = '${cfg.appId}'`,
  `export const FIREWALL_RULE_NAME = '${cfg.firewallRuleName}'`,
  '',
].join('\n')

writeFileSync(resolve(root, 'src/shared/branding.ts'), brandingTs, 'utf8')
console.log('✅ src/shared/branding.ts')

// 2) resources/installer.nsh（从模板生成）
const template = readFileSync(resolve(root, 'resources/installer.nsh.template'), 'utf8')
const installer = template
  .replace(/\{\{APP_NAME\}\}/g, cfg.appName)
  .replace(/\{\{PRODUCT_NAME\}\}/g, cfg.productName)
  .replace(/\{\{FIREWALL_RULE_NAME\}\}/g, cfg.firewallRuleName)

writeFileSync(resolve(root, 'resources/installer.nsh'), installer, 'utf8')
console.log('✅ resources/installer.nsh')

// 3) package.json
const pkgPath = resolve(root, 'package.json')
const pkg = JSON.parse(readFileSync(pkgPath, 'utf8'))
pkg.description = cfg.description
pkg.build.appId = cfg.appId
pkg.build.productName = cfg.productName
writeFileSync(pkgPath, JSON.stringify(pkg, null, 2) + '\n', 'utf8')
console.log('✅ package.json (description / build.appId / build.productName)')

// 4) src/renderer/index.html
const indexPath = resolve(root, 'src/renderer/index.html')
const indexHtml = readFileSync(indexPath, 'utf8')
const nextIndexHtml = indexHtml.replace(/<title>[\s\S]*?<\/title>/i, `<title>${cfg.appName}</title>`)
writeFileSync(indexPath, nextIndexHtml, 'utf8')
console.log('✅ src/renderer/index.html (title)')

console.log('\n🎀 Branding applied from app.config.mjs')
